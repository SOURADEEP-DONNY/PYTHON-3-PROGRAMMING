#BREAK STATEMENT
i=0
while i<=10:
    if i==5:
        break
    print(i)
    i=i+1

#WHEN THE CONDITION MATCHES THE BREAK STATEMENT THE PROGRAM COMES OUT FROM THE LOOP CONDITION.
print('\n\n')

#CONTINUE STATEMENT
i=0
while i<=10:
    if i==5:
        continue
    print(i)        
    i=i+1
    

