#TUPLES ARE JUST LIKE LISTS.
#DIFFERENCE BETWEEN TUPLES AND LISTS IS TUPLES ARE IMMUTABLE & LISTS ARE MUTABLE.
# i.e. ONCE DECLARED TUPLES CANNOT BE CHANGED.

mylist=["one",2,"three"]
mytuple=("one",2,"three")

#----PRINTING THE TYPES-----------
print(type(mylist))
print(type(mytuple))

#-----PRINTING THE VALUES----------
print(mylist)
print(mytuple)
