# 'in' & 'not in' FUNTIONS

print('a' in 'pineapple')
print('z' in 'pineapple')
print('' in 'pineapple')

#THOUGH 'a' IS PRESENT BUT STILL AN ITEM IN THE LIST NEEDS TO BE PRESENT FOR THE EXPRESSION TO BE TRUE

print('a' in ['apple', 'altro', 'pineapple', 'BMW'])
print('a' in ['apple', 'altro', 'pineapple'])

#CLEARLY AN ITEM NEEDS TO MATCH IN THE LIST TO BE TRUE

print('apple' in ['apple', 'altro', 'pineapple'])
print('apple' in ['apple', 'altro', 'pineapple', 'BMW'])


print('a' not in 'pineapple')
print('z' not in 'pineapple')
print('' not in 'pineapple')



print('a' not in ['apple', 'altro', 'pineapple', 'BMW'])
print('a' not in ['apple', 'altro', 'pineapple'])


print('apple' not in ['apple', 'altro', 'pineapple'])
print('apple' not in ['apple', 'altro', 'pineapple', 'BMW'])
